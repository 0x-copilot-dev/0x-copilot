function TabBar({
  active,
  onSelect,
}: {
  active: LibraryItemKind;
  onSelect: (kind: LibraryItemKind) => void;
}): ReactElement {
  const wrapper: CSSProperties = {
    display: "flex",
    gap: 4,
    borderBottom: `1px solid ${PANEL_BORDER}`,
    paddingBottom: 0,
  };
  return (
    <div role="tablist" aria-label="Library kinds" style={wrapper}>
      {TABS.map((descriptor) => {
        const isActive = descriptor.tab === active;
        const buttonStyle: CSSProperties = {
          height: 36,
          padding: "0 14px",
          borderRadius: 0,
          border: "none",
          background: "transparent",
          color: isActive ? TEXT_PRIMARY : TEXT_SECONDARY,
          fontSize: 13,
          fontWeight: isActive ? 600 : 500,
          cursor: "pointer",
          borderBottom: `2px solid ${isActive ? ACCENT : "transparent"}`,
          marginBottom: -1,
        };
        return (
          <button
            key={descriptor.tab}
            type="button"
            role="tab"
            aria-selected={isActive}
            aria-controls={`library-panel-${descriptor.tab}`}
            id={`library-tab-${descriptor.tab}`}
            data-testid={`library-tab-${descriptor.tab}`}
            onClick={() => onSelect(descriptor.tab)}
            style={buttonStyle}
          >
            {descriptor.label}
          </button>
        );
      })}
    </div>
  );
}

export function LibraryDestination(): ReactElement {
  const transport = useTransport();
  const router = useRouter<ArtifactRoute>();
  const [active, setActive] = useState<LibraryItemKind>("adapter");
  const [state, setState] = useState<ViewState>({ kind: "loading" });
  const [reloadToken, setReloadToken] = useState(0);

  useEffect(() => {
    let cancelled = false;
    setState({ kind: "loading" });
    transport
      .request<LibraryResponse>({
        method: "GET",
        path: "/v1/library",
        query: { kind: active },
      })
      .then((response) => {
        if (cancelled) return;
        setState({ kind: "ready", items: response.items });
      })
      .catch((error: unknown) => {
        if (cancelled) return;
        const message =
          error instanceof Error ? error.message : "Network error";
        setState({ kind: "error", message });
      });
    return () => {
      cancelled = true;
    };
  }, [transport, active, reloadToken]);

  const handleSelectTab = useCallback((next: LibraryItemKind) => {
    setActive(next);
  }, []);

  const handleRetry = useCallback(() => {
    setReloadToken((t) => t + 1);
  }, []);

  const handleOpen = useCallback(
    (item: LibraryItem) => {
      router.navigate({ kind: "workspace", workspaceId: item.id });
    },
    [router],
  );

  const descriptor = TABS.find((t) => t.tab === active) ?? TABS[0]!;

  const rootStyle: CSSProperties = {
    width: "100%",
    height: "100%",
    minHeight: 0,
    backgroundColor: APP_BACKGROUND,
    color: TEXT_PRIMARY,
    boxSizing: "border-box",
    display: "flex",
    flexDirection: "column",
    overflow: "auto",
  };
  const containerStyle: CSSProperties = {
    width: "100%",
    maxWidth: 1000,
    margin: "0 auto",
    padding: "24px 28px 48px",
    boxSizing: "border-box",
    display: "flex",
    flexDirection: "column",
    gap: 20,
  };
  const titleStyle: CSSProperties = {
    fontSize: 20,
    fontWeight: 600,
  };
  const listStyle: CSSProperties = {
    display: "flex",
    flexDirection: "column",
    gap: 10,
  };

  let body: ReactElement | null = null;
  if (state.kind === "loading") {
    body = (
      <div style={listStyle} data-testid="library-list" data-state="loading">
        {Array.from({ length: SKELETON_ROW_COUNT }).map((_, i) => (
          <SkeletonRow key={i} index={i} />
        ))}
      </div>
    );
  } else if (state.kind === "error") {
    body = <ErrorPanel message={state.message} onRetry={handleRetry} />;
  } else if (state.items.length === 0) {
    body = (
      <EmptyPanel title={descriptor.emptyTitle} hint={descriptor.emptyHint} />
    );
  } else {
    body = (
      <div style={listStyle} data-testid="library-list" data-state="ready">
        {state.items.map((item) => (
          <ItemRow key={item.id} item={item} onOpen={handleOpen} />
        ))}
      </div>
    );
  }

  return (
    <section
      aria-label="Library destination"
      data-testid="library-destination"
      data-state={state.kind}
      data-active-tab={active}
      style={rootStyle}
    >
      <div style={containerStyle}>
        <div style={titleStyle}>Library</div>
        <TabBar active={active} onSelect={handleSelectTab} />
        <div
          role="tabpanel"
          id={`library-panel-${active}`}
          aria-labelledby={`library-tab-${active}`}
        >
          {body}
        </div>
      </div>
    </section>
  );
}
