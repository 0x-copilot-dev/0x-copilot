import {
  useCallback,
  useEffect,
  useState,
  type CSSProperties,
  type ReactElement,
} from "react";

import type { LibraryItemId } from "@enterprise-search/api-types";

import { useRouter } from "../../providers/RouterProvider";
import { useTransport } from "../../providers/TransportProvider";
import type { ArtifactRoute } from "../../routing/router";
import { formatRelativeTime } from "../../util/time";

export type LibraryItemKind = "adapter" | "result" | "knowledge";

export interface LibraryItem {
  readonly id: LibraryItemId;
  readonly kind: LibraryItemKind;
  readonly title: string;
  readonly modifiedAt: string;
  readonly subtitle?: string;
}

interface LibraryResponse {
  readonly items: ReadonlyArray<LibraryItem>;
}

type ViewState =
  | { readonly kind: "loading" }
  | { readonly kind: "error"; readonly message: string }
  | { readonly kind: "ready"; readonly items: ReadonlyArray<LibraryItem> };

interface TabDescriptor {
  readonly tab: LibraryItemKind;
  readonly label: string;
  readonly slug: string;
  readonly emptyTitle: string;
  readonly emptyHint: string;
}

const TABS: ReadonlyArray<TabDescriptor> = [
  {
    tab: "adapter",
    label: "Adapters",
    slug: "adapter",
    emptyTitle: "No saved adapters",
    emptyHint:
      "Tier-2 adapters generated by the agent and saved in your workspace will appear here.",
  },
  {
    tab: "result",
    label: "Results",
    slug: "result",
    emptyTitle: "No saved results",
    emptyHint: "Tool results pinned by the agent will land here for re-use.",
  },
  {
    tab: "knowledge",
    label: "Knowledge",
    slug: "knowledge",
    emptyTitle: "No knowledge cards",
    emptyHint:
      "Cards distilled from chats and runs will appear here as the agent builds context.",
  },
];

